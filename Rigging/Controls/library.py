    def createControlShape(self,shape, name='',offset=(0,0,0),rotation=(0,0,0),size=1.0):
        """Creates a control shape
        All shapes lay on the xz plane and point down -z, towards the origin, when applicable

        Args:
            shape (str): name of the shape to create
            name (str, optional): Name to be used as the control's basename. Defaults to ''.
            offset ([float], optional): Offset location of control shape. Defaults to (0,0,0).
            rotation ([float] (angle), optional): Rotation, in angles, to adjust the placement of the control. Defaults to (0,0,0).
            size (float, optional): Scale Adjustment. Defaults to 1.0.


        Returns:
            str: control name
        """
        # Get dict
        controlDict = self.getControlData(shape)
        # Generate Control
        if controlDict['type']=='curve':
            controlNode = curve(
                n=controlDict['n'],
                d=controlDict['d'],
                p=controlDict['p'],
                k=controlDict['k'])

        elif controlDict['type']=='circle':
            controlNode = circle(
                n=controlDict['n'],
                c=controlDict['c'],
                nr=controlDict['nr'],
                sw=controlDict['sw'],
                r=controlDict['r'],
                d=controlDict['d'],
                ut=controlDict['ut'],
                tol=controlDict['tol'],
                s=controlDict['s'],
                ch=controlDict['ch'])

        elif controlDict['type']== 'nSphere':
            if isinstance(size,tuple):
                size=size[0]

            controlNode=sphere(
                n=controlDict['n'],
                nsp=controlDict['spans'],
                s=controlDict['sections'],
                cch=controlDict['cch'],
                r=size)[0]

        elif controlDict['type']=='special':
            controlNode = mc_eval(controlDict['mel'])
            controlNode = ls(selection=1)
        else:
            raise KeyError("Generation of {} type not supported currently.".format(controlDict['type']))
            
        # Transform
        if isinstance(size,(tuple,list)):
            xform(controlNode,s=size,r=1)
        else:
            size = (size,size,size)
            xform(controlNode,s=size,r=1)
        xform(controlNode,ro=rotation,r=1) #orient control
        xform(controlNode,t=offset,r=1)
        makeIdentity(controlNode, apply=True)

        #rename if given a name
        if name:
            controlNode = rename(controlNode, name)

        return controlNode


